package Condiciones;

public class Estructura_ifelse {
    public static void main(String[] args) {
        /*
         * Un usuario se logea en la plataforma y desea saver
         * saber si lo logro o no
         * */
        //declarar una variable
        boolean login = true;
        //usaremos condicional
        if(login){
            System.out.println("Se logeo correctamente");
        }else{
            System.out.println("No se logeo correctamente");
        }
    }

}
